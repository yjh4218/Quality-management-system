-- V27: 제조사 Audit 그룹별 평가 기능 추가 및 템플릿 필드 보완
CREATE TABLE IF NOT EXISTS manufacturer_audit_group_results (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    audit_id BIGINT NOT NULL,
    group_id BIGINT NOT NULL,
    feedback TEXT,
    FOREIGN KEY (audit_id) REFERENCES manufacturer_audits(id) ON DELETE CASCADE,
    FOREIGN KEY (group_id) REFERENCES audit_template_groups(id)
);

-- AuditTemplate에 target_category 컬럼 추가 (존재하지 않을 경우 대비)
ALTER TABLE audit_templates ADD COLUMN IF NOT EXISTS target_category VARCHAR(100);
